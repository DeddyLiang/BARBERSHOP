    public void WriteToDatabase(String name, String username, String password, String email) throws SQLException {
        try {

            String database = "jdbc:mysql://localhost:3306/barbershop.login";
            System.out.println("Connection Passed");
            Statement sta = conn.createStatement();

            String insertSql = "INSERT INTO barbershop.login"
                    + "(name,username,password,email)"
                    + "VALUES (?,?,?,?);";
            PreparedStatement pstatement = conn.prepareStatement(insertSql);
            pstatement.setString(1, name);
            pstatement.setString(2, username);
            pstatement.setString(3, password);
            pstatement.setString(4, email);
            pstatement.executeUpdate();

            System.out.println("Record insert.");
            pstatement.close();
//            conn.close();
            System.out.println("Inserted to DB !");

        } catch (Exception e) {
            System.out.println(e);
        }

    }

