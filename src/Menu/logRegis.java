        if (txtName.getText().equals("") || txtUsername.getText().equals("") || jPassword.getText().equals("") || txtEmail.getText().equals("")) {
            util.Sutil.msg(this, "Cannot empty!");
        } else {
            try {
                lr.WriteToDatabase(txtName.getText(), txtUsername.getText(), jPassword.getText(), txtEmail.getText());

            } catch (SQLException ex) {
                Logger.getLogger(logRegis.class.getName()).log(Level.SEVERE, null, ex);
            }
            util.Sutil.msg(this, "Register Success");

            dispose();
        }


