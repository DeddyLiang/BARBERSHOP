    private void databaseConnection() {
        try {
            conn = DriverManager.getConnection(DbConn.JDBC_URL,
                    DbConn.JDBC_USERNAME,
                    DbConn.JDBC_PASSWORD);
            if (conn != null) {
                System.out.println("Connected to DB!");
            }
        } catch (SQLException ex) {
            System.out.println("Error:\n" + ex.getLocalizedMessage());
        }
    }

    private void loadDatabaseMember() {
        try {
            removeTableData();
            String sql = "SELECT * FROM barbershop.`member`";

            PreparedStatement pstatement = conn.prepareStatement(sql);
            ResultSet rs = pstatement.executeQuery();
            if (rs.isBeforeFirst()) { // check is resultset not empty
                DefaultTableModel tableModel = (DefaultTableModel) tblRoster.getModel();

                while (rs.next()) {

                    Object data[] = {
                        rs.getInt("memberid"),
                        rs.getString("name"),
                        rs.getString("handphoneno"),
                        rs.getString("address"),
                        rs.getString("membersince")
                    };
                    tableModel.addRow(data);
                }
            } else {
                Sutil.msg(this, "Record empty.");
            }

            rs.close();
            pstatement.close();
        } catch (SQLException ex) {
            System.out.println("Error:\n" + ex.getLocalizedMessage());
        }
    }

    private void removeTableData() {
        DefaultTableModel tableModel = (DefaultTableModel) tblRoster.getModel();
        tableModel.setRowCount(0);
    }

    private void tableSelectionListener() {

        ListSelectionListener listener = new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent e) {
                int row = tblRoster.getSelectedRow();
                if (row >= 0) {
                    txtMemberID2.setText(tblRoster.getValueAt(row, 0).toString());
                    txtMemberName.setText(tblRoster.getValueAt(row, 1).toString());
//                    txtAddress.setText(tblRoster.getValueAt(row, 2).toString());
//                    txtHandphoneNO.setText(tblRoster.getValueAt(row, 3).toString());
                }
            }
        };
        tblRoster.getSelectionModel().setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tblRoster.getSelectionModel().addListSelectionListener(listener);
    }

