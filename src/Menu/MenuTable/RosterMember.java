    private void executeUpdate() throws SQLException {
        String updateSql = "UPDATE barbershop.member SET "
                + "handphoneno=?,address=?";
        PreparedStatement pstatement = conn.prepareStatement(updateSql);

//        pstatement.setString(1, (txtHandphoneNO.getText()));
//        pstatement.setString(2, (txtAddress.getText()));

        pstatement.executeUpdate();
        pstatement.close();
        executeNew();
        Sutil.msg(this, "Update successful.");

        loadDatabaseMember();
    }

    private void executeDelete() throws SQLException {
        String updateSql = "DELETE FROM barbershop.member WHERE memberid = ? ";
        PreparedStatement pstatement = conn.prepareStatement(updateSql);
        pstatement.setInt(1, Integer.valueOf(txtMemberID2.getText().trim()));
        pstatement.executeUpdate();
        pstatement.close();
        executeNew();
        Sutil.msg(this, "Delete successful.");

        loadDatabaseMember();
    }

